#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { loadEffectiveOpenApi, repositoryRoot } from './openapi-loader.mjs';

const operationOutput = path.join(repositoryRoot, 'frontend/genealogy-web/src/shared/api/generated/api-contract.ts');
const trackingTypesOutput = path.join(repositoryRoot, 'frontend/genealogy-web/src/shared/api/generated/tracking-types.ts');
const methods = ['get', 'post', 'put', 'patch', 'delete'];
const trackingSchemaNames = [
  'OperationLogResponse',
  'OperationLogPage',
  'OperationLogStatsItem',
  'OperationLogStatsResponse',
  'CheckTaskResponse',
  'AuditRecordResponse',
  'ReviewTaskDetailResponse',
  'FieldDiff',
  'ReviewDiffResponse'
];

function refName(schema) {
  if (!schema) return null;
  if (schema['$ref']) return schema['$ref'].split('/').pop();
  if (schema.items && schema.items['$ref']) return `${schema.items['$ref'].split('/').pop()}[]`;
  return null;
}

function requestBodyName(operation) {
  const content = operation.requestBody?.content || {};
  return refName(content['application/json']?.schema || content['multipart/form-data']?.schema);
}

function responseName(operation) {
  const response = operation.responses?.['200'] || operation.responses?.['201'] || operation.responses?.default || {};
  return refName(response.content?.['application/json']?.schema);
}

function resolveParameter(openapi, parameter) {
  const reference = parameter?.['$ref'];
  const prefix = '#/components/parameters/';
  if (!reference || !reference.startsWith(prefix)) {
    return parameter;
  }
  const name = reference.slice(prefix.length);
  return openapi.components?.parameters?.[name] || parameter;
}

function paramsOf(openapi, pathItem, operation, location) {
  return [...(pathItem.parameters || []), ...(operation.parameters || [])]
    .map(item => resolveParameter(openapi, item))
    .filter(item => item.in === location)
    .map(item => item.name)
    .filter(Boolean)
    .sort();
}

function collectOperations(openapi) {
  const result = {};
  const allPaths = openapi.paths || {};
  for (const route of Object.keys(allPaths).sort()) {
    const pathItem = allPaths[route] || {};
    for (const method of methods) {
      const operation = pathItem[method];
      if (!operation) continue;
      const key = `${method.toUpperCase()} ${route}`;
      result[key] = {
        operationId: operation.operationId || key.replace(/[^a-zA-Z0-9]+/g, '_'),
        method: method.toUpperCase(),
        path: route,
        pathParams: paramsOf(openapi, pathItem, operation, 'path'),
        queryParams: paramsOf(openapi, pathItem, operation, 'query'),
        requestBody: requestBodyName(operation),
        response: responseName(operation)
      };
    }
  }
  return result;
}

function renderOperations(operations) {
  return `/* eslint-disable */\n/**\n * Auto-generated by scripts/api/generate-frontend-client.mjs from docs/api/openapi.json and overlays.\n * Do not edit manually.\n */\n\nexport const API_OPERATIONS = ${JSON.stringify(operations, null, 2)} as const;\n\nexport type ApiOperation = keyof typeof API_OPERATIONS;\nexport type ApiMethod<K extends ApiOperation = ApiOperation> = typeof API_OPERATIONS[K]['method'];\nexport type ApiPath<K extends ApiOperation = ApiOperation> = typeof API_OPERATIONS[K]['path'];\nexport type ApiPathParamName<K extends ApiOperation> = typeof API_OPERATIONS[K]['pathParams'][number];\nexport type ApiQueryParamName<K extends ApiOperation> = typeof API_OPERATIONS[K]['queryParams'][number];\nexport type ApiPathParams<K extends ApiOperation> = [ApiPathParamName<K>] extends [never] ? never : Record<ApiPathParamName<K>, string | number>;\nexport type ApiQueryParams<K extends ApiOperation> = [ApiQueryParamName<K>] extends [never] ? never : Partial<Record<ApiQueryParamName<K>, string | number | boolean | undefined>>;\nexport type ApiRequestBodySchema<K extends ApiOperation> = typeof API_OPERATIONS[K]['requestBody'];\nexport type ApiResponseSchema<K extends ApiOperation> = typeof API_OPERATIONS[K]['response'];\n`;
}

function propertyName(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function arrayItemType(type) {
  return type.includes(' | ') || type.includes(' & ') ? `(${type})` : type;
}

function schemaToType(schema) {
  if (!schema) return 'unknown';

  let type;
  if (schema['$ref']) {
    type = schema['$ref'].split('/').pop();
  } else if (Array.isArray(schema.enum) && schema.enum.length > 0) {
    type = schema.enum.map(value => JSON.stringify(value)).join(' | ');
  } else if (Array.isArray(schema.oneOf) && schema.oneOf.length > 0) {
    type = schema.oneOf.map(schemaToType).join(' | ');
  } else if (Array.isArray(schema.anyOf) && schema.anyOf.length > 0) {
    type = schema.anyOf.map(schemaToType).join(' | ');
  } else if (Array.isArray(schema.allOf) && schema.allOf.length > 0) {
    type = schema.allOf.map(schemaToType).join(' & ');
  } else if (schema.type === 'array') {
    type = `${arrayItemType(schemaToType(schema.items))}[]`;
  } else if (schema.type === 'integer' || schema.type === 'number') {
    type = 'number';
  } else if (schema.type === 'boolean') {
    type = 'boolean';
  } else if (schema.type === 'string') {
    type = 'string';
  } else if (schema.type === 'object' || schema.properties) {
    const required = new Set(schema.required || []);
    const properties = Object.entries(schema.properties || {});
    if (properties.length === 0) {
      type = schema.additionalProperties
        ? `Record<string, ${schemaToType(schema.additionalProperties)}>`
        : 'Record<string, unknown>';
    } else {
      const fields = properties.map(([name, property]) => {
        const optional = required.has(name) ? '' : '?';
        return `${propertyName(name)}${optional}: ${schemaToType(property)};`;
      });
      type = `{ ${fields.join(' ')} }`;
    }
  } else {
    type = 'unknown';
  }

  return schema.nullable ? `${type} | null` : type;
}

function renderNamedSchema(name, schema) {
  if (schema.type !== 'object' && !schema.properties) {
    return `export type ${name} = ${schemaToType(schema)};`;
  }

  const required = new Set(schema.required || []);
  const fields = Object.entries(schema.properties || {}).map(([property, propertySchema]) => {
    const optional = required.has(property) ? '' : '?';
    return `  ${propertyName(property)}${optional}: ${schemaToType(propertySchema)};`;
  });
  return `export type ${name} = {\n${fields.join('\n')}\n};`;
}

function renderTrackingTypes(openapi) {
  const schemas = openapi.components?.schemas || {};
  const missing = trackingSchemaNames.filter(name => !schemas[name]);
  if (missing.length > 0) {
    throw new Error(`Missing tracking schemas in effective OpenAPI: ${missing.join(', ')}`);
  }

  const body = trackingSchemaNames
    .map(name => renderNamedSchema(name, schemas[name]))
    .join('\n\n');

  return `/* eslint-disable */\n/**\n * Auto-generated tracking-center DTOs from the effective OpenAPI contract.\n * Do not edit manually.\n */\n\n${body}\n`;
}

function writeGeneratedFile(file, content) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, content, 'utf8');
}

const { openapi, overlayFiles } = loadEffectiveOpenApi();
writeGeneratedFile(operationOutput, renderOperations(collectOperations(openapi)));
writeGeneratedFile(trackingTypesOutput, renderTrackingTypes(openapi));

const overlays = overlayFiles.length ? ` with ${overlayFiles.join(', ')}` : '';
console.log(`Generated ${path.relative(repositoryRoot, operationOutput)} and ${path.relative(repositoryRoot, trackingTypesOutput)} from docs/api/openapi.json${overlays}`);
