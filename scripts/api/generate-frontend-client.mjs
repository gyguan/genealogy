#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dir = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(dir, '../..');
const apiDir = path.join(root, 'docs/api');
const input = path.join(apiDir, 'openapi.json');
const output = path.join(root, 'frontend/genealogy-web/src/shared/api/generated/api-contract.ts');
const methods = ['get', 'post', 'put', 'patch', 'delete'];

function refName(schema) {
  if (!schema) return null;
  if (schema['$ref']) return schema['$ref'].split('/').pop();
  if (schema.items && schema.items['$ref']) return `${schema.items['$ref'].split('/').pop()}[]`;
  return null;
}

function requestBodyName(operation) {
  const content = operation.requestBody?.content || {};
  return refName(content['application/json']?.schema || content['multipart/form-data']?.schema);
}

function responseName(operation) {
  const response = operation.responses?.['200'] || operation.responses?.['201'] || operation.responses?.default || {};
  return refName(response.content?.['application/json']?.schema);
}

function paramsOf(pathItem, operation, location) {
  return [...(pathItem.parameters || []), ...(operation.parameters || [])]
    .filter(item => item.in === location)
    .map(item => item.name)
    .filter(Boolean)
    .sort();
}

function mergeComponents(baseComponents = {}, overlayComponents = {}) {
  const merged = { ...baseComponents, ...overlayComponents };
  for (const section of ['schemas', 'parameters', 'responses', 'requestBodies', 'headers', 'securitySchemes', 'links', 'callbacks']) {
    merged[section] = {
      ...(baseComponents[section] || {}),
      ...(overlayComponents[section] || {})
    };
    if (Object.keys(merged[section]).length === 0) {
      delete merged[section];
    }
  }
  return merged;
}

function loadEffectiveOpenApi() {
  const base = JSON.parse(fs.readFileSync(input, 'utf8'));
  const overlayFiles = fs.readdirSync(apiDir)
    .filter(filename => /^openapi\..+\.json$/.test(filename))
    .sort();

  for (const filename of overlayFiles) {
    const overlay = JSON.parse(fs.readFileSync(path.join(apiDir, filename), 'utf8'));
    base.paths = { ...(base.paths || {}), ...(overlay.paths || {}) };
    base.components = mergeComponents(base.components, overlay.components);
  }

  return { openapi: base, overlayFiles };
}

function collect(openapi) {
  const result = {};
  const allPaths = openapi.paths || {};
  for (const route of Object.keys(allPaths).sort()) {
    const pathItem = allPaths[route] || {};
    for (const method of methods) {
      const operation = pathItem[method];
      if (!operation) continue;
      const key = `${method.toUpperCase()} ${route}`;
      result[key] = {
        operationId: operation.operationId || key.replace(/[^a-zA-Z0-9]+/g, '_'),
        method: method.toUpperCase(),
        path: route,
        pathParams: paramsOf(pathItem, operation, 'path'),
        queryParams: paramsOf(pathItem, operation, 'query'),
        requestBody: requestBodyName(operation),
        response: responseName(operation)
      };
    }
  }
  return result;
}

function render(operations) {
  return `/* eslint-disable */\n/**\n * Auto-generated by scripts/api/generate-frontend-client.mjs from docs/api/openapi.json and overlays.\n * Do not edit manually.\n */\n\nexport const API_OPERATIONS = ${JSON.stringify(operations, null, 2)} as const;\n\nexport type ApiOperation = keyof typeof API_OPERATIONS;\nexport type ApiMethod<K extends ApiOperation = ApiOperation> = typeof API_OPERATIONS[K]['method'];\nexport type ApiPath<K extends ApiOperation = ApiOperation> = typeof API_OPERATIONS[K]['path'];\nexport type ApiPathParamName<K extends ApiOperation> = typeof API_OPERATIONS[K]['pathParams'][number];\nexport type ApiQueryParamName<K extends ApiOperation> = typeof API_OPERATIONS[K]['queryParams'][number];\nexport type ApiPathParams<K extends ApiOperation> = [ApiPathParamName<K>] extends [never] ? never : Record<ApiPathParamName<K>, string | number>;\nexport type ApiQueryParams<K extends ApiOperation> = [ApiQueryParamName<K>] extends [never] ? never : Partial<Record<ApiQueryParamName<K>, string | number | boolean | undefined>>;\nexport type ApiRequestBodySchema<K extends ApiOperation> = typeof API_OPERATIONS[K]['requestBody'];\nexport type ApiResponseSchema<K extends ApiOperation> = typeof API_OPERATIONS[K]['response'];\n`;
}

const { openapi, overlayFiles } = loadEffectiveOpenApi();
const operations = collect(openapi);
fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, render(operations), 'utf8');
const overlays = overlayFiles.length ? ` with ${overlayFiles.join(', ')}` : '';
console.log(`Generated ${path.relative(root, output)} from ${path.relative(root, input)}${overlays}`);
